%
% Calculate total product output from make table
% q = tpom(V)
% 
% returns total product output as column vector
%
% Need in workspace: 
% V = make table (industry by product) at basic price = matrix
%
function q = tpom(V)

    q_transpose = sum(V, 1);
    q = transpose(q_transpose);

endfunction